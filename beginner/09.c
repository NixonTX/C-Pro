#include <stdio.h>
#include<math.h>

int main()
{
    printf("%f \n",6+5.5);
    printf("%d \n",6/5);    //ignores the decimal//
    printf("%f \n",6/5.0);
    
    int num=6;
    printf("%f \n",num+5.5);
    
    printf("%f \n",pow(2,3));   //2^3//
    printf("%f \n",sqrt(16));
    printf("%f \n",ceil(16.0002));
    printf("%f \n",floor(16.9999));
    

    return 0;
}

