//Bitwise Operators- & Bitwise AND, | Bitwise OR,  ^ Bitwise XOR, ~ Bitwise NOT, << Leftshift, >> Rightshift
//Decimal to Binary then to Decimal(The output) 
#include<stdio.h>
int main(){
    int a=10, b=5;
    printf("\nBitwise AND %d",a&b);
    printf("\nBitwise OR %d",a|b);
    printf("\nBitwise XOR %d",a^b);

    return 0;
}